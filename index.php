        function do_login() {
            var username = $("#username").val();
            var password = $("#password").val();
            if (username != "" && password != "") {
                $("#loading_spinner").css({
                    "display": "block"
                });
                $.ajax({
                    type: 'post',
                    url: 'login_api/loginn.php',
                    data: {
                        do_login: "do_login",
                        username: username,
                        password: password
                    },
                    success: function(response) {

                        if (response == 1) {
                            // alert('Login Succesfully');
                            window.location.href = "users.php";
                        } else {
                            // alert('Your Login Name or Password is invalid');
                            Swal.fire({
                                title: "Oops...",
                                text: "Invalid Username Or Password",
                                icon: "error",
                                confirmButtonClass: "btn btn-primary w-xs mt-2",
                                buttonsStyling: !1,
                                // footer: '<a href="">Why do I have this issue?</a>',
                                showCloseButton: !0
                            })
                            $("#loading_spinner").css({
                                "display": "none"
                            });
                        }
                    }
                });
            } else {
                // alert("Please Fill All The Details");
                Swal.fire({
                    html: '<div class="mt-3"><lord-icon src="https://cdn.lordicon.com/tdrtiskw.json" trigger="loop" colors="primary:#f06548,secondary:#f7b84b" style="width:120px;height:120px"></lord-icon><div class="mt-4 pt-2 fs-15"><h4>Oops...! Something went Wrong !</h4><p class="text-muted mx-4 mb-0">Please Fill All Details</p></div></div>',
                    showCancelButton: !0,
                    showConfirmButton: !1,
                    cancelButtonClass: "btn btn-primary w-xs mb-1",
                    cancelButtonText: "Dismiss",
                    buttonsStyling: !1,
                    showCloseButton: !0
                })
            }

            return false;
        }
    </script>
